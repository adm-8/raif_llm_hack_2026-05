def _make_request(raw_text: str) -> str:
    categories_block = "\n\n".join(f"=== {name} ===\n{definition}" for name, definition in CATEGORY_DEFINITIONS.items())
    allowed = " | ".join(CATEGORIES)
    return f"""Ты — классификатор атак на банковский чат-бот. Изучи полную сессию диалога между пользователем и оператором/чатботом технической поддержки и выбери ровно ОДНУ категорию red-flag из списка.

{GENERAL_RULES}

{PRIORITY_RULES}

ОПРЕДЕЛЕНИЯ КАТЕГОРИЙ:

{categories_block}

ФОРМАТ ОТВЕТА:
Возвращай СТРОГО JSON-объект вида {{"category": "<одно из значений>"}} и ничего больше.
Допустимые значения category: {allowed}.

Диалог для анализа:
{raw_text}
"""


def _parse_category(response_text: str | None) -> str | None:
    if not response_text:
        return None
    try:
        payload = json.loads(response_text)
    except (ValueError, TypeError):
        return None
    if not isinstance(payload, dict):
        return None
    category = payload.get("category")
    if not isinstance(category, str):
        return None
    category = category.strip()
    if category not in CATEGORIES:
        return None
    return category


def _classify_with_retries(llm_client: LLMClient, raw_text: str) -> str | None:
    """Запрашивает LLM с до MAX_RETRIES повторами при невалидном ответе.

    Возвращает распарсенную категорию из CATEGORIES или None, если все попытки провалились.
    """
    prompt = _make_request(raw_text)
    total_attempts = MAX_RETRIES + 1
    last_response: str | None = None
    for attempt in range(1, total_attempts + 1):
        last_response = llm_client.request_completion(prompt, json_mode=True)
        category = _parse_category(last_response)
        if category is not None:
            return category
        if attempt < total_attempts:
            detection_logger.warning(
                "LLM вернула невалидный ответ (попытка %d/%d). Raw: %r — повтор",
                attempt,
                total_attempts,
                (last_response or "")[:500],
            )
    detection_logger.warning(
        "LLM вернула невалидный ответ после %d попыток. Last raw: %r — clear",
        total_attempts,
        (last_response or "")[:500],
    )
    return None


def run_llm_detection(
    llm_client: LLMClient,
    raw_text: str,
) -> dict[str, typing.Any] | None:
    """Entry point used by app.models.process_risk_detection.

    Returns ``{"category": ...}`` for a detected red flag, or ``None`` for clear.
    """
    try:
        category = _classify_with_retries(llm_client, raw_text)
    except Exception:
        detection_logger.exception("Детекция упала — возвращаю clear")
        return None

    if category is None or category == CLEAR_CATEGORY:
        return None
    return {"category": category}


def _format_confusion_matrix(
    y_true: list[str],
    y_pred: list[str],
    labels: list[str],
) -> str:
    index = {label: i for i, label in enumerate(labels)}
    matrix = [[0] * len(labels) for _ in labels]
    for t, p in zip(y_true, y_pred, strict=True):
        if t in index and p in index:
            matrix[index[t]][index[p]] += 1

    col_width = max(6, max(len(label) for label in labels) + 1)
    row_label_width = max(len(label) for label in labels) + 4

    header = " " * row_label_width + "".join(f"{label[: col_width - 1]:>{col_width}}" for label in labels)
    lines = ["Confusion matrix (rows=true, cols=pred):", header]
    for label, row in zip(labels, matrix, strict=True):
        row_str = "".join(f"{cell:>{col_width}}" for cell in row)
        lines.append(f"{label:<{row_label_width}}{row_str}")
    return "\n".join(lines)


def _evaluate(n: int | None) -> int:
    if not os.getenv("OPENROUTER_API_KEY"):
        print("OPENROUTER_API_KEY не задан — прогон невозможен. Установи переменную окружения.")
        return 1
    if not _TRAIN_PATH.exists():
        print(f"Не найден файл {_TRAIN_PATH}")
        return 1

    from sklearn.metrics import accuracy_score, classification_report  # noqa: PLC0415
    from tqdm import tqdm  # type: ignore[import-untyped]  # noqa: PLC0415

    raw = json.loads(_TRAIN_PATH.read_text(encoding="utf-8"))
    conversations = [Conversation.from_dict(item) for item in raw]
    total_loaded = len(conversations)
    if n is not None and n < total_loaded:
        conversations = random.sample(conversations, n)
    print(f"Прогон по {len(conversations)} диалогам (из {total_loaded} в train.json)")

    client = LLMClient()

    def _predict(conv: Conversation) -> tuple[str, str, bool]:
        try:
            category = _classify_with_retries(client, conv.as_string)
        except Exception:
            detection_logger.exception("Ошибка предсказания для %s", conv.session_id)
            return conv.category, CLEAR_CATEGORY, True
        if category is None:
            return conv.category, CLEAR_CATEGORY, True
        return conv.category, category, False

    y_true: list[str] = []
    y_pred: list[str] = []
    invalid_count = 0

    with concurrent.futures.ThreadPoolExecutor(max_workers=8) as pool:
        futures = [pool.submit(_predict, conv) for conv in conversations]
        for fut in tqdm(
            concurrent.futures.as_completed(futures),
            total=len(futures),
            desc="LLM classify",
        ):
            true_cat, pred_cat, invalid = fut.result()
            y_true.append(true_cat)
            y_pred.append(pred_cat)
            if invalid:
                invalid_count += 1

    print()
    print(f"Всего: {len(y_true)}, accuracy: {accuracy_score(y_true, y_pred):.4f}")
    print(f"Невалидных ответов LLM (после {MAX_RETRIES} ретраев): {invalid_count}")
    print()
    print(classification_report(y_true, y_pred, labels=CATEGORIES, zero_division=0))
    print(_format_confusion_matrix(y_true, y_pred, CATEGORIES))
    return 0


def _main(argv: list[str]) -> int:
    logging.basicConfig(level=logging.WARNING, format="%(levelname)s %(name)s: %(message)s")
    n: int | None = None
    if len(argv) > 1:
        try:
            n = int(argv[1])
        except ValueError:
            print(f"Аргумент N должен быть числом, получено: {argv[1]!r}")
            return 1
        if n <= 0:
            print("N должен быть положительным")
            return 1
    return _evaluate(n)


if __name__ == "__main__":
    sys.exit(_main(sys.argv))
